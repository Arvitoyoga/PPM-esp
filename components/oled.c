/* ================= PRINT ================= */

static void oled_char(char c)
{
    uint8_t index;

    if(c >= '0' && c <= '9')
        index = c - '0';

    else if(c >= 'A' && c <= 'Z')
        index = (c - 'A') + 10;

    else if(c >= 'a' && c <= 'z')
        index = (c - 'a') + 36;

    else if(c == ' ')
        index = 62;

    else if(c == '.')
        index = 63;

    else if(c == ':')
        index = 64;

    else if(c == '-')
        index = 65;

    else if(c == '/')
        index = 66;

    else if(c == '%')
        index = 67;

    else
        return;

    for(int i=0;i<5;i++)
        oled_data(font[index][i]);

    oled_data(0x00);

    cursor_x += 6;
}

void oled_draw_bitmap(const uint8_t *bitmap, uint8_t w, uint8_t h)
{
    for(int page = 0; page < h/8; page++)
    {
        oled_set_cursor(0, page);

        for(int col = 0; col < w; col++)
        {
            oled_data(bitmap[page * w + col]);
        }
    }
}

void oled_print(char *str)
{
    while(*str)
    {
        oled_char(*str);
        str++;
    }
}