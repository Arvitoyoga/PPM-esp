#define I2C_PORT I2C_NUM_0
#define SDA_PIN 21
#define SCL_PIN 22

#define OLED_ADDR 0x3C

static uint8_t cursor_x = 0;
static uint8_t cursor_y = 0;

/* ================= I2C ================= */

static void i2c_init()
{
    i2c_config_t conf = {
        .mode = I2C_MODE_MASTER,
        .sda_io_num = SDA_PIN,
        .scl_io_num = SCL_PIN,
        .sda_pullup_en = GPIO_PULLUP_ENABLE,
        .scl_pullup_en = GPIO_PULLUP_ENABLE,
        .master.clk_speed = 100000
    };

    i2c_param_config(I2C_PORT, &conf);
    i2c_driver_install(I2C_PORT, conf.mode, 0, 0, 0);
}

/* ================= OLED LOW LEVEL ================= */

static void oled_cmd(uint8_t cmd)
{
    i2c_cmd_handle_t h = i2c_cmd_link_create();

    i2c_master_start(h);
    i2c_master_write_byte(h, (OLED_ADDR << 1) | I2C_MASTER_WRITE, true);
    i2c_master_write_byte(h, 0x00, true);
    i2c_master_write_byte(h, cmd, true);
    i2c_master_stop(h);

    i2c_master_cmd_begin(I2C_PORT, h, pdMS_TO_TICKS(100));
    i2c_cmd_link_delete(h);
}

static void oled_data(uint8_t data)
{
    i2c_cmd_handle_t h = i2c_cmd_link_create();

    i2c_master_start(h);
    i2c_master_write_byte(h, (OLED_ADDR << 1) | I2C_MASTER_WRITE, true);
    i2c_master_write_byte(h, 0x40, true);
    i2c_master_write_byte(h, data, true);
    i2c_master_stop(h);

    i2c_master_cmd_begin(I2C_PORT, h, pdMS_TO_TICKS(100));
    i2c_cmd_link_delete(h);
}

/* ================= OLED ================= */

void oled_set_cursor(uint8_t x, uint8_t y)
{
    cursor_x = x;
    cursor_y = y;

    oled_cmd(0xB0 + y);                 // pilih page
    oled_cmd(0x00 + (x & 0x0F));        // lower column
    oled_cmd(0x10 + ((x >> 4) & 0x0F)); // higher column
}

void oled_clear()
{
    for(int page=0; page<8; page++)
    {
        oled_set_cursor(0,page);

        for(int i=0;i<128;i++)
            oled_data(0x00);
    }

    oled_set_cursor(0,0);
}

